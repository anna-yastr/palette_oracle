const TYPE_NAMES = Object.keys(PREDICTION_TYPES);

function countSentences(text) {
  return (text.match(/[.!?]+/g) || []).length;
}

function generateLore(excludePrimaries = new Set()) {
  const availableTypes = TYPE_NAMES.filter(t =>
    PREDICTION_TYPES[t].some(line => !excludePrimaries.has(line))
  );
  const typePool = availableTypes.length > 0 ? availableTypes : TYPE_NAMES;
  const typeName = typePool[Math.floor(Math.random() * typePool.length)];

  const available = PREDICTION_TYPES[typeName].filter(line => !excludePrimaries.has(line));
  const pool      = available.length > 0 ? available : PREDICTION_TYPES[typeName];
  const primary   = pool[Math.floor(Math.random() * pool.length)];

  const primaryCount = countSentences(primary);
  const remaining    = 3 - primaryCount;
  const roll         = Math.random();

  // ~0.2% chance: three lines — только если primary = 1 предложение
  if (roll < 0.002 && remaining >= 2) {
    const otherTypes  = TYPE_NAMES.filter(t => t !== typeName);
    const shuffled    = otherTypes.sort(() => Math.random() - 0.5);
    const secPool     = PREDICTION_TYPES[shuffled[0]].filter(l => countSentences(l) === 1);
    const terPool     = PREDICTION_TYPES[shuffled[1]].filter(l => countSentences(l) === 1);
    if (secPool.length > 0 && terPool.length > 0) {
      const secondary = secPool[Math.floor(Math.random() * secPool.length)];
      const tertiary  = terPool[Math.floor(Math.random() * terPool.length)];
      return { text: primary + '\n' + secondary + '\n' + tertiary, typeName, primaryLine: primary };
    }
  }

  // ~3% chance: две строки — только если есть место
  if (roll < 0.032 && remaining >= 1) {
    const otherTypes = TYPE_NAMES.filter(t => t !== typeName);
    const otherPool  = PREDICTION_TYPES[otherTypes[Math.floor(Math.random() * otherTypes.length)]];
    const secPool    = otherPool.filter(l => countSentences(l) <= remaining);
    if (secPool.length > 0) {
      const secondary = secPool[Math.floor(Math.random() * secPool.length)];
      return { text: primary + '\n' + secondary, typeName, primaryLine: primary };
    }
  }

  return { text: primary, typeName, primaryLine: primary };
}

module.exports = { generateLore };
